const mongoose = require("mongoose");
const Schema = mongoose.Schema;
// Create Schema
const UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  role: {
    type: String,
    default: 'user',
    enum: ['user', 'manager']
  }
},
  {
    timestamps: true
  },
  {
    collection: 'users'
}
);
module.exports = mongoose.model("users", UserSchema);